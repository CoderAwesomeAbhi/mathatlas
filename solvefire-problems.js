// Build SF_CONTESTS lookup — keyed by contest_id
// Used by the Contest Simulator, sidebar tiles, and exam mode
// ═══════════════════════════════════════════════════════════
(function buildSFContests() {
  const map = {};
  const levelMap = {
    "AMC 8":       "AMC 8",
    "AMC 8–10":    "AMC 8–10",
    "AMC 10":      "AMC 10",
    "AMC 10–12":   "AMC 10–12",
    "AMC 12–AIME": "AMC 12–AIME",
    "AIME":        "AIME",
  };
  window.SF_PROBLEMS.forEach(function(p) {
    // Normalise: expose .problem alias if only .text was set (defensive)
    if (!p.problem && p.text) p.problem = p.text;
    if (!p.text && p.problem) p.text = p.problem;
    // Build contest entry
    if (!map[p.contest_id]) {
      map[p.contest_id] = {
        id:       p.contest_id,
        name:     p.contest_name,
        short:    p.display.replace(/ #\d+$/, ""),
        date:     p.date,
        level:    levelMap[p.level] || p.level,
        problems: [],
      };
    }
    map[p.contest_id].problems.push(p);
  });
  // Sort each contest's problems by num
  Object.values(map).forEach(function(c) {
    c.problems.sort(function(a, b) { return a.num - b.num; });
  });
  window.SF_CONTESTS = map;
  window.SF_PROBLEMS_LOADED = true;
})();
